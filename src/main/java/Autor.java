public class Autor {
    int id;
    String nombre;
    String nacionalidad;
    String alias;
    String fecha;

    public Autor(int id, String nombre, String nacionalidad, String alias, String fecha){
        this.id = id;
        this.nombre= nombre;
        this.nacionalidad = nacionalidad;
        this.alias = alias;
        this.fecha = fecha;
    }

    public int getId() {
        return id;
    }

    public String getNacionalidad() {
        return nacionalidad;
    }

    public String getNombre() {
        return nombre;
    }
}
